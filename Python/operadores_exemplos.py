'''
    Operadores aritméticos

+  = adição
-  = subtração
*  = multiplicação
/  = divisão
** = potenciação
// = divisão inteira
%  = resto da divisão
== = Resultado (na linguágem de python dois sinais de igual significa "resultado")
'''

'''
    Exemplos

5+2  == 7
5-2  == 3
5*2  == 10
5/2  == 2.5
5**2 == 25
5//2 == 2
5%2  == 1
'''

'''
    Precedência

    1 = () Parêntezes
        2 = **
            3 =  *, /, //, %
                4 = +, -s
'''